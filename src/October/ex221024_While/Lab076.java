package October.ex221024_While;

public class Lab076 {
    public static void main(String[] args) {
        int i =10;
        while (i>=1){
            System.out.println(i);
            i--;
        }

        System.out.println("==================");

        int j =0;
        while (j<10){
            System.out.println(j);
            j++;
        }

    }
}
