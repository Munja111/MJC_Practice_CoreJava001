package October.ex211024_Loop;

public class Lab065 {
    public static void main(String[] args) {
        for (int raj = 0; raj < 10; raj++) {
            System.out.println(raj); // Print the -> 0-9

            System.out.println(); // for space
            for (int i = 1; i <=10 ; ++i) {  // we can incre/decre -> pre-post does not matter
                System.out.println(i); // Print the -> 1-10

            }

        }
    }
}
