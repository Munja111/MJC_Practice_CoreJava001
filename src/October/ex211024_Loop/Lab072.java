package October.ex211024_Loop;

public class Lab072 {
    public static void main(String[] args) {
        System.out.println("Start the loop");
        for (int i = 0; i < 10 ;i++) { // 10 Times (0-9) -> Mjc
            System.out.println("Mjc "+i);
        }
        System.out.println("=========== Midpoint");

        for (int i = 1; i <= 10 ;i++) { // 10 Times (1-10) -> Mjc
            System.out.println("Mjc "+i);
        }
        System.out.println("end the Loop");
    }
}
