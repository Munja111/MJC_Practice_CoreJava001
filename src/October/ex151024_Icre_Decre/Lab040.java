package October.ex151024_Icre_Decre;

public class Lab040 {
    public static void main(String[] args) {
        int a=5;
        System.out.println(a++);  //5
        System.out.println(++a); // 7
        System.out.println(a--); // 7
        System.out.println(--a); // 5
        System.out.println();

        int m=5;
        int n=++m;

        System.out.println(n);
    }
}
