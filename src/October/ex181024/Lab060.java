package October.ex181024;

public class Lab060 {
    public static void main(String[] args) {
        char code = 'C';
        switch (code){  // whatever value  we set inside  parenthesis will display
            default:
                System.out.println("Hello World!!!!!!!!!!!!!");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
